package fr.algorithmie;

public class AffichageSuite {
	public static void main(String[] args) {
		for(int a=1 ; a <= 10 ;a++) {
			System.out.println(a);
			
		}
		for(int a=0 ; a<=10 ;a+=2) {
			System.out.println(a);
		}
		for(int a=1 ; a<=9 ;a+=2) {
			System.out.println(a);
		}
		int num=1;
		while (num <= 10) {
			System.out.println(num);
			num++;
		}
		int nm=0;
		while (nm <= 10) {
			System.out.println(nm);
			nm+=2;
		}
		int nm1=1;
		while (nm1 < 10) {
			System.out.println(nm1);
			nm1+=2;
		}
		
	}

}
